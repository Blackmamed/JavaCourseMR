package Gun14;

public class _02_forLoop {
    public static void main(String[] args) {
        // 1 den 5e kadar olan sayilari ekrana yazdir

        // dongunun kac kere doneceyi belli olmayan durumlarda kallanacaig

        int i=0;
        while (i<5)
        {
            System.out.println("i = " + i);
            i++;
        }

        // for dongusu basi belli sonu belli artisi belli

        for (i=1; i<5; i++)
        {
            System.out.println("i = " + i);
        }


    }

}
